    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int selectedRow = tbl_data.getSelectedRow();
        if(selectedRow==-1){
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan diperbarui");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(confirm==JOptionPane.YES_OPTION){
            String id= tbl_data.getValueAt(selectedRow, 0).toString();
            try {
                String query = "Delete from mahasiswa where id=?";
                PreparedStatement st = conn.prepareStatement(query);
                st.setString(1, id);
                int rowDeleted = st.executeUpdate();
                if(rowDeleted>0){
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                    resetForm();getData();
                }
                st.close();
            } catch (Exception e) {
                Logger.getLogger(FormMhs.class.getName()).log(Level.SEVERE, null,e);
            }
        }
        resetForm();getData();
        aktifinButton();
        nonAktifButton();
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void t_pencarianKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t_pencarianKeyTyped
        DefaultTableModel model = (DefaultTableModel) tbl_data.getModel();
        model.setRowCount(0);
        
        String keyCari = t_pencarian.getText();
        
        try {
            String query = "SELECT * FROM mahasiswa WHERE nama LIKE ? OR alamat LIKE ? OR telepon LIKE ?";
            PreparedStatement st = conn.prepareStatement(query);
            st.setString(1, "%"+ keyCari + "%");
            st.setString(2, "%"+ keyCari + "%");
            st.setString(3, "%"+ keyCari + "%");
            ResultSet rs = st.executeQuery();
            
            while (rs.next()) {                
                int id         = rs.getInt("id");
                String nama    = rs.getString("nama");
                String telepon = rs.getString("telepon");
                String alamat  = rs.getString("alamat");
                
                Object rowData[] ={id,nama,telepon,alamat};
                model.addRow(rowData);
                
            }
            rs.close();
            st.close();
        } catch (Exception e) {
            Logger.getLogger(FormMhs.class.getName()).log(Level.SEVERE, null,e);
        }
       
    }//GEN-LAST:event_t_pencarianKeyTyped

