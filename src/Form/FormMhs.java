    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        String nama = t_nama.getText();
        String telepon = t_telepon.getText();
        String alamat = t_alamat.getText();
        
        if(nama.isEmpty()||telepon.isEmpty()||alamat.isEmpty()){
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi", "validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String query = "INSERT INTO mahasiswa (nama, telepon,alamat) values (?,?,?)";
            PreparedStatement st = conn.prepareStatement(query);
            st.setString(1, nama);
            st.setString(2, telepon);
            st.setString(3, alamat);
            
            int rowInserted  =st.executeUpdate();
            if(rowInserted>0){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
                resetForm();
                getData();
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(FormMhs.class.getName()).log(Level.SEVERE, null,e);
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int selectedRow = tbl_data.getSelectedRow();
        if(selectedRow==-1){
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan diperbarui");
            return;
        }
        String id = tbl_data.getValueAt(selectedRow, 0).toString();
        String nama  = t_nama.getText();
        String telepon = t_telepon.getText();
        String alamat= t_alamat.getText();
        
        if(nama.isEmpty()||telepon.isEmpty()||alamat.isEmpty()){
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi", "validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            String query = "Update mahasiswa set nama=?, telepon=?, alamat=? where id=?";
            PreparedStatement st = conn.prepareStatement(query);
            st.setString(1, nama);
            st.setString(2, telepon);
            st.setString(3, alamat);
            st.setString(4, id);
            
            int rowUpdated  =st.executeUpdate();
            if(rowUpdated>0){
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
                
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(FormMhs.class.getName()).log(Level.SEVERE, null,e);
        }
        resetForm();
        getData();
        aktifinButton();nonAktifButton();
    }//GEN-LAST:event_btn_updateActionPerformed

